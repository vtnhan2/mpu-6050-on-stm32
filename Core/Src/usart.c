// UART Debug functions implementation
void UART_SendString(const char* str)
{
    HAL_UART_Transmit(&huart2, (uint8_t*)str, strlen(str), HAL_MAX_DELAY);
}

void UART_SendChar(char c)
{
    HAL_UART_Transmit(&huart2, (uint8_t*)&c, 1, HAL_MAX_DELAY);
}

void UART_NewLine(void)
{
    UART_SendString("\r\n");
}

void UART_ClearScreen(void)
{
    UART_SendString("\033[2J\033[H"); // ANSI escape sequence to clear screen
}

void UART_Printf(const char* format, ...)
{
    char buffer[256];
    va_list args;
    va_start(args, format);
    vsnprintf(buffer, sizeof(buffer), format, args);
    va_end(args);
    UART_SendString(buffer);
}

// printf support for UART
int _write(int file, char *ptr, int len)
{
    HAL_UART_Transmit(&huart2, (uint8_t*)ptr, len, HAL_MAX_DELAY);
    return len;
}

// MPU6050 Display functions
void UART_DisplayAccelerometer(float ax, float ay, float az)
{
    UART_Printf("Accelerometer: X=%.3f, Y=%.3f, Z=%.3f m/s^2\r\n", ax, ay, az);
}

void UART_DisplayGyroscope(float gx, float gy, float gz)
{
    UART_Printf("Gyroscope:     X=%.3f, Y=%.3f, Z=%.3f rad/s\r\n", gx, gy, gz);
}

void UART_DisplayTemperature(float temp)
{
    UART_Printf("Temperature:   %.2f C\r\n", temp);
}

void UART_DisplayAngles(float roll, float pitch, float yaw)
{
    UART_Printf("Angles:        Roll=%.2f, Pitch=%.2f, Yaw=%.2f deg\r\n", roll, pitch, yaw);
}

void UART_DisplayMPU6050Data(void)
{
    UART_ClearScreen();
    UART_SendString("=== MPU6050 Sensor Data ===\r\n");
    UART_NewLine();
}

